package com.example.NutriTrack.utils;

public class FoodInfo {
    private String name;
    private double calories_per_unit;
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public double getCalories_per_unit() {
        return calories_per_unit;
    }
    public void setCalories_per_unit(double calories_per_unit) {
        this.calories_per_unit = calories_per_unit;
    }
}
